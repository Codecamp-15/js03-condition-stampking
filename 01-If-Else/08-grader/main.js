// - จงเขียนโค้ดเพื่อรับคะแนนมาคำนวณเกรด
//     - ถ้าคะแนน มากกว่าเท่ากับ 80 ได้ A
//         - ถ้าคะแนน อยู่ระหว่าง 70 - 79 ได้ B
//             - ถ้าคะแนน อยู่ระหว่าง 60 - 69 ได้ C
//                 - ถ้าคะแนน อยู่ระหว่าง 50 - 59 ได้ D
//                     - ถ้าคะแนน น้อยกว่า 50 ได้ F

let result = 100;

if (result >= 80) {
    console.log("A");
}
else if (result >= 79) {
    console.log("B")
}

else if (result >= 69) {
    console.log("C");
} else if (result >= 59)
    console.log("D");
